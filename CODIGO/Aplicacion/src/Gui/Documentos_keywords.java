    public void ListarDoid() throws ClassNotFoundException, SQLException {
        //Como el m√©todo de la BL retorna un ArrayList de la clase Estudiantes
        //Aqui lo recuperamos
        String dbpedia = "'http://dbpedia.org/resource/" + (String) this.jComboBox1.getSelectedItem() + "'";
        keyword.setDbepedia(dbpedia);
        ArrayList<Keywords> lstdoi = blkey.ConsultaLista2(keyword);
        al.clear();
        for (Keywords objeto : lstdoi) {
            String dois = "'" + objeto.getDoi() + "'";
            al.add(dois);

        }
        ale.clear();
        for (int i = 0; i < al.size(); i++) {
            String doiss = (String) al.get(i);
            document.setDoi(doiss);
            lstestudiante = bldocument.ConsultaLista2(document);
            for (Documentos objeto : lstestudiante) {
                String doises = "'" + objeto.getDocumento() + "'";

                ale.add(doises);
            }

        }
        Object columnas[] = {"Nombre del documento"};
        DefaultTableModel modelo = new DefaultTableModel(null, columnas);
        jTable1.setModel(modelo);

        modelo.removeTableModelListener(jTable1);
       Collections.sort(ale);
        for (int i = 0; i < ale.size(); i++) {
            String nombres = (String) ale.get(i);
            String modeloTemp[] = {nombres};
            modelo.addRow(modeloTemp);

        }

    }

